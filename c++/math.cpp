#include <iostream>
#include <cmath>
using namespace std;

int main(){
    cout << max(4, 2) << endl; //returns max value between the two elements (4)
    cout << min(1, 40) << endl; //returns min value (1)
    cout << sqrt(64) << endl;
    cout << round(2.4) << endl; //round to the nearest integer
    cout << log(2.7188); //log is the natural log
}
